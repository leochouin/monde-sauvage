const TEXT_ORIGINALS = new WeakMap();
const ATTR_ORIGINALS = new WeakMap();

const normalizeForLookup = (value) => value
  .normalize('NFD')
  .replace(/[\u0300-\u036f]/g, '')
  .replace(/[’]/g, "'")
  .trim();

const translateText = (sourceText) => {
  const normalized = normalizeForLookup(sourceText);
  const direct = EXACT_TRANSLATIONS[normalized];
  if (direct) return direct;

  // Keep original text when we do not have an explicit phrase mapping.
  // This avoids mixed-language strings (partial word replacements).
  return sourceText;
};

const shouldSkipElement = (element) => {
  if (!element) return true;
  const tagName = element.tagName;
  if (!tagName) return true;
  const upper = tagName.toUpperCase();
  return upper === 'SCRIPT' || upper === 'STYLE' || upper === 'TEXTAREA' || upper === 'CODE' || upper === 'PRE';
};

const TEXT_ATTRIBUTES = ['placeholder', 'title', 'aria-label', 'alt', 'value'];

const applyTranslationToTextNode = (node, language) => {
  if (!node || !node.parentElement || shouldSkipElement(node.parentElement)) return;

  if (!TEXT_ORIGINALS.has(node)) {
    TEXT_ORIGINALS.set(node, node.nodeValue || '');
  }

  const originalValue = TEXT_ORIGINALS.get(node) || '';
  const nextValue = language === 'en' ? translateText(originalValue) : originalValue;
  if (node.nodeValue !== nextValue) {
    node.nodeValue = nextValue;
  }
};

const applyTranslationToElementAttributes = (element, language) => {
  if (!element || shouldSkipElement(element)) return;

  if (!ATTR_ORIGINALS.has(element)) {
    const originals = {};
    TEXT_ATTRIBUTES.forEach((attributeName) => {
      if (element.hasAttribute(attributeName)) {
        originals[attributeName] = element.getAttribute(attributeName) || '';
      }
    });
    ATTR_ORIGINALS.set(element, originals);
  }

  const originals = ATTR_ORIGINALS.get(element) || {};
  Object.entries(originals).forEach(([attributeName, originalValue]) => {
    const nextValue = language === 'en' ? translateText(originalValue) : originalValue;
    if (element.getAttribute(attributeName) !== nextValue) {
      element.setAttribute(attributeName, nextValue);
    }
  });
};

const translateTree = (rootNode, language) => {
  if (!rootNode) return;

  const elementRoot = rootNode.nodeType === Node.ELEMENT_NODE ? rootNode : rootNode.parentElement;
  if (elementRoot) {
    applyTranslationToElementAttributes(elementRoot, language);
    elementRoot.querySelectorAll('*').forEach((element) => {
      applyTranslationToElementAttributes(element, language);
    });
  }

  const walker = document.createTreeWalker(rootNode, NodeFilter.SHOW_TEXT);
  let currentNode = walker.nextNode();
  while (currentNode) {
    applyTranslationToTextNode(currentNode, language);
    currentNode = walker.nextNode();
  }
};

export const installRuntimeTranslation = ({ root = document.body, language = 'fr' } = {}) => {
  if (typeof document === 'undefined' || !root) {
    return () => {};
  }

  let isApplying = false;

  const runTranslation = (node = root) => {
    if (isApplying) return;
    isApplying = true;
    try {
      translateTree(node, language);
    } finally {
      isApplying = false;
    }
  };

  runTranslation(root);

  const observer = new MutationObserver((mutations) => {
    if (isApplying) return;

    for (const mutation of mutations) {
      if (mutation.type === 'characterData') {
        runTranslation(mutation.target);
        continue;
      }

      if (mutation.type === 'attributes') {
        runTranslation(mutation.target);
        continue;
      }

      if (mutation.type === 'childList') {
        mutation.addedNodes.forEach((node) => {
          if (node.nodeType === Node.TEXT_NODE || node.nodeType === Node.ELEMENT_NODE) {
            runTranslation(node);
          }
        });
      }
    }
  });

  observer.observe(root, {
    subtree: true,
    childList: true,
    characterData: true,
    attributes: true,
    attributeFilter: TEXT_ATTRIBUTES,
  });

  return () => {
    observer.disconnect();
  };
};
